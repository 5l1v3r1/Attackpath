# Server string subroutine.
sub server {
my $X;
print "\n[ + ] Please wait, identifying server";
$probe = "string";
my $output;
my $webserver = "something";
&connect;
for ($X=0; $X<=10; $X++){
	$output = $results[$X];
	if (defined $output){
	if ($output =~/IIS/){ $webserver = "iis" };
	};
};
if ($webserver ne "iis"){
print "\n[ + ] Server IIS not confirmed, still continue [Y/N]";
my $choice = <STDIN>;
chomp $choice;
if ($choice =~/N/i) {&exit};
            }else{
print "\n[ + ] Server IIS confirmed, continuing!\n";
	};		
};  # end server subroutine.

# scan subroutine.
