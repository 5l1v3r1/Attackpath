}else{
print "\a\a\a\n[ + ] $host is vulernable $loop !!!";
     };
};
if ($status eq "not_vulnerable"){
				print "\n\n[ + ] $host is not vulernable, starting exit mode!\n";
				&exit;
				};
}; # end scan subroutine.

